import streamlit as st

def render():
    st.header("Урок 7: Отказоустойчивость (Durable Execution)")
    
    st.markdown("""
    В традиционных скриптах, если ваш код ловит Exception на середине пути, все накопленные переменные теряются, и скрипт падает.
    
    Но когда к вашему StateGraph привязан **Checkpointer** (например, `MemorySaver`), граф обретает **Долговечность (Durable Execution)**.
    Это означает, что LangGraph сохраняет прогресс в фоновом режиме на каждом шаге (после каждого узла).
    """)

    st.subheader("1. Защита от катастрофических сбоев")
    st.markdown("""
    Если узел упал посреди выполнения (например, сервер API перезагрузился):
    1. Исполнение графа останавливается на границе последнего целого шага.
    2. Все **Pending writes** (отложенные записи в состояние от других параллельных успешных узлов) сохраняются.
    3. При следующем вызове `graph.invoke()` с тем же `thread_id`, граф **не будет выполнять успешные узлы заново**, он начнет работу ровно с упавшего узла!
    """)
    
    st.code("""
# Пример: у нас есть параллельные задачи A, B, C
# A и C выполнились, а B упал с таймаутом

# Перезапуск:
config = {"configurable": {"thread_id": "123"}}
# LangGraph заметит, что A и C уже были завершены на этом супер-степе,
# и перезапустит ТОЛЬКО задачу B, сэкономив вам токены и время.
agent_app.invoke(None, config=config)
    """, language="python")

    st.subheader("2. Как это работает под капотом (Супер-шаги)")
    st.markdown("""
    LangGraph работает **Супер-шагами (Super-steps)**. 
    Один супер-шаг завершается только тогда, когда все узлы, активированные в данный момент, завершили свою работу и вернули словари обновлений.
    Если на супер-шаге всё хорошо, происходит запись состояния (Checkpoint).
    """)

    st.info("💡 **Вывод:** Доверить управление состоянием фреймворку LangGraph — значит обеспечить *Fault Tolerance* (устойчивость к отказам) из коробки. Разработчику нужно только добавить `checkpointer` при компиляции графа.")
