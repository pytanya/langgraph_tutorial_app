def render():
    st.header("Урок 18: Что под капотом? (Движок Pregel)")
    
    st.markdown("""
    Часто на собеседованиях задают вопрос: *Почему LangGraph так называется и как он работает быстрее обычных скриптов?*
    Секрет кроется в алгоритме **Pregel**, который работает на самом низком уровне фреймворка.
    """)

    st.subheader("Алгоритм Pregel (Google's Graph Algorithm)")
    st.markdown("""
    Pregel — это концепция крупномасштабной обработки графов, придуманная инженерами Google.
    Она базируется на парадигме **BSP (Bulk Synchronous Parallel)** — Крупноблочная синхронная параллельность.
    
    Как работает Pregel в рамках LangGraph:
    1. **Супер-шаг (Superstep):** Система собирает все сообщения (updates), отправленные узлам на прошлом шаге.
    2. **Массовый Вызов (Compute):** Все узлы, которые получили входящие ребра (Edges), просыпаются и **запускаются одновременно в фоне** (параллельно). Узел не знает ничего о других узлах, он просто видит свой State.
    3. **Барьер Синхронизации (Barrier):** Система ЖДЕТ, пока абсолютно все запущенные узлы не закончат свою работу.
    4. **Слияние (Merge):** Результаты из всех узлов сливаются в общий State (через ваши Редьюсеры).
    5. Переход к следующему Супер-шагу!
    """)
    
    st.code("""
# Если у вас такая архитектура графа:
# START -> [Node A]
# START -> [Node B]
# [Node A] + [Node B] -> [Node C]

# Благодаря движку Pregel под капотом, LangGraph запустит Узлы A и B абсолютно параллельно в разных потоках.
# А Узел C будет стоять перед Барьером (Barrier), ожидая, пока A и B не вернут результаты!
    """, language="python")

    st.warning("""
    Именно поэтому в LangGraph используется `operator.add` для редьюсеров в State! 
    Так как узлы отрабатывают параллельно в рамках одного супер-шага, их ответы должны сливаться (слипаться в массив), а не перезаписывать друг друга (Overwriting), иначе возникнет эффект гонки (Race Condition)!
    """)
